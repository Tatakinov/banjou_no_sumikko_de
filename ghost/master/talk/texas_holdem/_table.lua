  local s1, n1 = string.match(h1, "(%w)(%d+)")
  n1  = tonumber(n1)
  local s2, n2 = string.match(h2, "(%w)(%d+)")
  n2  = tonumber(n2)
  if n1 > n2 then
    n1, n2  = n2, n1
  end
  local suited  = s1 == s2
  for i, v in ipairs(table[n]) do
    if v.suit == suited and n1 == v.n1 and n2 == v.n2 then
      return i
    end
  end
  print(n, h1, h2)
  assert(false)
end

